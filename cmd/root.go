package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "photon",
	Short: "Modular Go framework with adapter support for Echo, Gin, and more",
	Long: `Photon is a modular Go framework designed to simplify backend development by leveraging
adapter patterns. It supports popular HTTP frameworks like Echo and Gin through adapters,
allowing developers to build scalable, maintainable web applications with clean abstractions.

The framework provides powerful CLI tools for code generation and project scaffolding,
empowering teams to accelerate development workflows and maintain high code quality.`,
	// Run: func(cmd *cobra.Command, args []string) { },
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

